#include <bits/stdc++.h>
using namespace std;

int main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	string s;
	int q;
	cin >> s >> q;
	for(long long k; q--; cout.put(' ')) {
		cin >> k;
		--k;
		auto d = k / s.size();
		k %= s.size();
		if(__builtin_popcountll(d) & 1)
			if('A' <= s[k] && s[k] <= 'Z')
				cout.put(s[k] - 'A' + 'a');
			else
				cout.put(s[k] - 'a' + 'A');
		else
			cout.put(s[k]);
	}
	cout << endl;
	return 0;
}
